int	validate_input(char *s)
{
	int	i;
	int	count;

	i = 0;
	count = 0;
	while (s[i])
	{
		if ((s[i] < '1' || s[i] > '4') && s[i] != ' ')
			return (0);
		if (s[i] >= '1' && s[i] <= '4')
			count++;
		i++;
	}
	if (count != 16)
		return (0);
	return (1);
}
